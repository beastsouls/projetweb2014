package projet.facture.model;

public class Liste {

	private String nom;
	
	private long qtt;
	
	private long som;
	
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public long getQtt() {
		return qtt;
	}
	public void setQtt(long qtt) {
		this.qtt = qtt;
	}
	public long getSom() {
		return som;
	}
	public void setSom(long som) {
		this.som = som;
	}
}
